    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
//Agregar mensaje de advertencia al modificar estudiante 10/03/23
        int confirmacion = JOptionPane.showConfirmDialog(this, "¬øEst√° seguro de modificar el estudiante?",
         "Registro"
         ,JOptionPane.OK_CANCEL_OPTION);
  if(confirmacion == 0){        
        //captura de datos del estudiante
        //int codigo = Integer.parseInt(lblId.getText());
        String nombre = txtNombre.getText();
        String correo = txtCorreo.getText();
        String apellido = txtApellido.getText();
         String telefono = txtTelefono.getText();
           String grado = txtGrado.getText();
         
        //asignando los campos al objeto EstudainteVO
       // estudianteVO.setEstId(codigo);
        estudianteVO.setEstNombre(nombre);
        estudianteVO.setEstApellido(apellido);
        estudianteVO.setEstCorreo(correo);
       
        estudianteVO.setEstTelefono(telefono);
        estudianteVO.setEstCurso(grado);
        
        boolean resultado = fachada.modificarEstudiante(estudianteVO);
        
        if(resultado){
            JOptionPane.showMessageDialog(null, "Estudiante actualizado correctamente","Actualizacion",JOptionPane.INFORMATION_MESSAGE);
            limpiar();
        }else{
            JOptionPane.showMessageDialog(null, "Error al intentar actualizar el Estudiante","Error",JOptionPane.ERROR_MESSAGE);
        }
  }
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnModificarActionPerformed

