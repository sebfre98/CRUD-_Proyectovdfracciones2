    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
       
        String idEst = txtEstCodigo.getText();
        
        estudianteVO = fachada.consultarEstudiante(idEst);
        
        txtNombre.setText(estudianteVO.getEstNombre());
        txtGrado.setText(estudianteVO.getEstCurso());
        txtTelefono.setText(estudianteVO.getEstTelefono());
        txtApellido.setText(estudianteVO.getEstApellido());
        txtCorreo.setText(estudianteVO.getEstCorreo());
        txtCodigo.setText(""+estudianteVO.getEstId());
        
    }//GEN-LAST:event_btnConsultarActionPerformed

