    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        String idEst = txtIdEstudiante.getText();
        
        estudianteVO = fachada.consultarEstudiante(idEst);
       
       lblNombre.setText(estudianteVO.getEstNombre());
       lblTelefono.setText(estudianteVO.getEstTelefono());
       lblApellido.setText(estudianteVO.getEstApellido());
       lblCodigo.setText(""+estudianteVO.getEstId());
       lblCorreo.setText(estudianteVO.getEstCorreo());
       lblGrado.setText(estudianteVO.getEstCurso());
       
        
    }//GEN-LAST:event_btnBuscarActionPerformed

