    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar el estudiante?","Eliminación",JOptionPane.OK_CANCEL_OPTION);
        
        if(confirmacion == 0){
            boolean resultado = fachada.eliminarEstudiante(estudianteVO);
            
            if(resultado){
                JOptionPane.showMessageDialog(null, "El estudiante fue eliminado correctamente","Eliminación",JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            }else{
                JOptionPane.showMessageDialog(null, "Ocurrió un error al intentar eliminar el estudiante","Eliminación",JOptionPane.ERROR_MESSAGE);
            }
            
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
