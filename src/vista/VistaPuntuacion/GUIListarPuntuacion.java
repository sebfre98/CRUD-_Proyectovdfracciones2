    private void limpiarTabla(){
        int numeroFila = tablaListarPuntuacion.getRowCount();
        
        for(int i = 0; i<numeroFila ; i++){
            DefaultTableModel modelo = (DefaultTableModel) tablaListarPuntuacion.getModel();
            modelo.removeRow(modelo.getRowCount() - 1);
        }
    }
    
