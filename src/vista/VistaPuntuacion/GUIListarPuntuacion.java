    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        List<PunuVO> listaPunuVO = fachada.consultarListaPunu();
        
        limpiarTabla();
        
        DefaultTableModel modelo = (DefaultTableModel) tablaListarPuntuacion.getModel();
        
        for(PunuVO punuVO : listaPunuVO){
            Object fila[] = {punuVO.getPunuId(), punuVO.getCodEst(), punuVO.getCodNiv(), punuVO.getPuntaje()};
            modelo.addRow(fila);
        }
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

