    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
//Mensaje de advertencia al querer modificar la puntuacion 10/03/23
        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Está seguro de modificar la puntuacion?",
         "Modificar puntuacion"
         ,JOptionPane.OK_CANCEL_OPTION);
  if(confirmacion == 0){        
        //captura de datos de la puntuacion
        int codigo = Integer.parseInt(txtPuntuacionID.getText());
        int estudiante= Integer.parseInt(txtEstudiante.getText());
         int nivel = Integer.parseInt(txtNivel.getText());
        int puntaje = Integer.parseInt(txtPuntaje.getText());
        
         
        //asignando los campos al objeto PunuVO
        
        punuVO.setPunuId(codigo);
        punuVO.setCodEst(estudiante);
         punuVO.setCodNiv(nivel);
        punuVO.setPuntaje(puntaje);
        
        boolean resultado = fachada.modificarPunu(punuVO);
        
        if(resultado){
            JOptionPane.showMessageDialog(null, "Puntuacion actualizado correctamente","Actualizacion",JOptionPane.INFORMATION_MESSAGE);
            limpiar();
        }else{
            JOptionPane.showMessageDialog(null, "Error al intentar actualizar la puntuacion","Error",JOptionPane.ERROR_MESSAGE);
        }
  }
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnModificarActionPerformed

