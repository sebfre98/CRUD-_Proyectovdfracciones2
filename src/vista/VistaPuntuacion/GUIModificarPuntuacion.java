       punuVO = fachada.consultarPunu(idPunu);
        
        txtEstudiante.setText(""+punuVO.getCodEst());
         txtPuntaje.setText(""+punuVO.getPuntaje());
        txtNivel.setText(""+punuVO.getCodNiv());
